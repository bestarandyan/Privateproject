package com.piaoguanjia.chargeclient;

public class ChargeBean {
	public String username;
	public String chargeid;
	public String amount;
	public String accountTypeDis;
	public String createTime;
	public String auditTime;
	public String status;
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getChargeid() {
		return chargeid;
	}
	public void setChargeid(String chargeid) {
		this.chargeid = chargeid;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getAccountTypeDis() {
		return accountTypeDis;
	}
	public void setAccountTypeDis(String accountTypeDis) {
		this.accountTypeDis = accountTypeDis;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getAuditTime() {
		return auditTime;
	}
	public void setAuditTime(String auditTime) {
		this.auditTime = auditTime;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
}
